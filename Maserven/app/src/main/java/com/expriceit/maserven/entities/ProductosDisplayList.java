public class ProductosDisplayList {

    private String mDescripcion;
    private String mCodigo_interno;
    private String mCodigo_alterno;
    private String mPvp;
    private String mStock;

    public String getmDescripcion() {
        return mDescripcion;
    }

    public void setmDescripcion(String mDescripcion) {
        this.mDescripcion = mDescripcion;
    }

    public String getmCodigo_interno() {
        return mCodigo_interno;
    }

    public void setmCodigo_interno(String mCodigo_interno) {
        this.mCodigo_interno = mCodigo_interno;
    }

    public String getmCodigo_alterno() {
        return mCodigo_alterno;
    }

    public void setmCodigo_alterno(String mCodigo_alterno) {
        this.mCodigo_alterno = mCodigo_alterno;
    }

    public String getmPvp() {
        return mPvp;
    }

    public void setmPvp(String mPvp) {
        this.mPvp = mPvp;
    }

    public String getmStock() {
        return mStock;
    }

    public void setmStock(String mStock) {
        this.mStock = mStock;
    }
}
